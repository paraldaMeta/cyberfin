# 北京标准时经度（东经120度）
BEIJING_LONGITUDE = 120.0


def get_city_coordinates(province: str, city: str) -> Optional[Tuple[float, float]]:
    """
    获取城市经纬度
    
    Args:
        province: 省份名称
        city: 城市名称
    
    Returns:
        (经度, 纬度) 或 None
    """
    # 直辖市特殊处理
    if province in ['北京', '天津', '上海', '重庆']:
        city = province
    
    if province in CITY_COORDINATES:
        cities = CITY_COORDINATES[province]
        if city in cities:
            return cities[city]
        # 尝试模糊匹配
        for c_name, coords in cities.items():
            if city in c_name or c_name in city:
                return coords
    
    # 尝试直接在所有城市中查找
    for prov, cities in CITY_COORDINATES.items():
        if city in cities:
            return cities[city]
        for c_name, coords in cities.items():
            if city in c_name or c_name in city:
                return coords
    
    return None


def calculate_true_solar_time(
    birth_time_hour: int,
    birth_time_minute: int,
    longitude: float
) -> Tuple[int, int]:
    """
    计算真太阳时
    
    北京时间是东经120度的地方平时，需要根据出生地经度校正。
    每差1度，时间差4分钟（东加西减）
    
    Args:
        birth_time_hour: 北京时间小时
        birth_time_minute: 北京时间分钟
        longitude: 出生地经度
    
    Returns:
        (真太阳时小时, 真太阳时分钟)
    """
    # 计算经度差（与东经120度的差值）
    longitude_diff = BEIJING_LONGITUDE - longitude
    
    # 每1度差4分钟
    time_diff_minutes = longitude_diff * 4
    
    # 计算总分钟数
    total_minutes = birth_time_hour * 60 + birth_time_minute - time_diff_minutes
    
    # 处理跨日情况
    while total_minutes < 0:
        total_minutes += 24 * 60
    while total_minutes >= 24 * 60:
        total_minutes -= 24 * 60
    
    true_hour = int(total_minutes // 60)
    true_minute = int(total_minutes % 60)
    
    return true_hour, true_minute


def get_shichen_from_hour(hour: int) -> str:
    """
    根据小时获取时辰地支
    
    Args:
        hour: 小时 (0-23)
    
    Returns:
        时辰地支
    """
    shichen_map = {
        (23, 0): '子',
        (1, 2): '丑',
        (3, 4): '寅',
        (5, 6): '卯',
        (7, 8): '辰',
        (9, 10): '巳',
        (11, 12): '午',
        (13, 14): '未',
        (15, 16): '申',
        (17, 18): '酉',
        (19, 20): '戌',
        (21, 22): '亥',
    }
    
    if hour == 23 or hour == 0:
        return '子'
    
    for (start, end), zhi in shichen_map.items():
        if start <= hour <= end:
            return zhi
    
    return '子'  # 默认值


